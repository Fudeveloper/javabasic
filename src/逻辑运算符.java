public class 逻辑运算符 {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
//        AND运算符
        if (a == 1 && b == 2) {
            System.out.println(true);
        }

//        OR运算符
        if (a == 2 || b == 2) {
            System.out.println(true);
        }


    }
}
